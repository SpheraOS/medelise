/** Strip diacritics and lowercase */
function toUsername(prenume: string, nume: string): string {
    const strip = (s: string) =>
        s
            .normalize('NFD')
            .replace(/[\u0300-\u036f]/g, '')
            .replace(/[^a-zA-Z]/g, '')
            .toLowerCase();
    const p = strip(prenume);
    const n = strip(nume);
    return p || n ? `@${p}${n}` : '';
}

